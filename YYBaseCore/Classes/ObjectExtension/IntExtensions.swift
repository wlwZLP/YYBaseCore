import Foundation

public extension Int {
    
    /// Int 值转 CGFloat
    var cgFloat: CGFloat {
        return CGFloat(self)
    }
    
    /// Int 值转 Float
    var float: Float {
        return Float(self)
    }
    
    /// Int 值转 String
    var string: String {
        return String(self)
    }
    
    /// Int 值转 Double
    var double: Double {
        return Double(self)
    }
    
}
